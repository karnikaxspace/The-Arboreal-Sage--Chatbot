@app.route('/api/chat', methods=['POST'])
def chat():
    data = request.get_json()
    user_message = data.get('message', '').strip()
    if not user_message:
        return jsonify({'error': 'Message is required'}), 400

    # Use session ID to identify the user
    sid = session.get('_id')
    if not sid:
        sid = str(uuid.uuid4())
        session['_id'] = sid

    # ---- Name detection ----
    name = is_name_intro(user_message)
    if name:
        save_memory(sid, 'userName', name)

    # ---- 1. Active workflow? ----
    if is_workflow_active(sid):
        result = advance_workflow(sid, user_message)
        if result and result.get('done'):
            wf_type = result['type']
            answers = result['answers']
            if wf_type == 'DAILY_DIG' and answers.get('objective'):
                save_memory(sid, 'mainObjective', answers['objective'])
                save_memory(sid, 'mainObjectiveStatus', 'in-progress')

            # Build a synthesis prompt for the AI
            if wf_type == 'DAILY_DIG':
                prompt = (f"The user's Main Objective is: \"{answers.get('objective')}\". "
                          f"Obstacle: \"{answers.get('obstacle')}\". "
                          f"As the Arboreal Sage, suggest ONE small, concrete 'Seed' — a single action — they can plant today. "
                          f"Respond in 2-3 sentences with nature metaphors.")
            else:  # REFLECTION
                prompt = (f"User's Challenge: \"{answers.get('challenge')}\". "
                          f"Sunlight found: \"{answers.get('sunlight')}\". "
                          f"As the Arboreal Sage, offer ONE 'Growth Point' — a short, actionable insight. "
                          f"Respond in 2-3 sentences with nature metaphors.")

            ai_reply = ai_service.generate_response(prompt, get_all_memories(sid), None)
            summary = generate_workflow_summary(wf_type, answers)
            full_reply = f"{summary}\n\n{ai_reply}"
            clear_workflow(sid)
            return jsonify({'reply': full_reply})

        elif result and not result.get('done'):
            return jsonify({'reply': result['prompt']})
        else:
            clear_workflow(sid)
            return jsonify({'reply': "Let's start over. What would you like to do?"})

    # ---- 2. Workflow start intent ----
    wf_intent = detect_workflow_intent(user_message)
    if wf_intent:
        prompt = start_workflow(sid, wf_intent)
        if prompt:
            return jsonify({'reply': f"🌱 Let's begin {WORKFLOWS[wf_intent]['name']}. {prompt}"})
        else:
            return jsonify({'reply': "I couldn't start that workflow. Please try again."})

    # ---- 3. Memory recall intent ----
    if is_memory_check(user_message):
        obj = get_memory(sid, 'mainObjective')
        if obj:
            prompt = f"The user is asking about their Main Objective: \"{obj}\". Warmly ask how it's taking root, in 1-2 nature-metaphored sentences."
            reply = ai_service.generate_response(prompt, get_all_memories(sid), None)
            return jsonify({'reply': reply})
        else:
            return jsonify({'reply': "You haven't set a root objective yet. Would you like to start a Daily Dig?"})

    # ---- 4. Free chat ----
    wf_context = None
    state = get_workflow(sid)
    if state and state.get('is_active'):
        wf_context = {'type': state['type'], 'prompt': get_current_prompt(sid)}

    memories = get_all_memories(sid)
    reply = ai_service.generate_response(user_message, memories, wf_context)
    return jsonify({'reply': reply})

