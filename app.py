# 3. AI Service (mock + optional OpenAI)
# ============================================================
class MockAI:
    def generate_response(self, user_message, memories, workflow_context):
        mem_str = '. '.join([f"{k}: {v}" for k, v in memories.items()]) or 'No memories yet.'
        reply = f"🌳 The Arboreal Sage remembers: {mem_str}. "
        if workflow_context:
            reply += f"You are in the middle of a \"{workflow_context.get('type')}\" workflow. "
            reply += f"Please answer: {workflow_context.get('prompt')}"
        else:
            reply += f"You asked: \"{user_message}\". "
            reply += "Let’s explore that with the patience of a growing tree. "
            reply += "Would you like to start a Daily Dig or a Reflection session?"
        return reply

class OpenAIService:
    def __init__(self):
        self.client = OpenAI(api_key=os.getenv('OPENAI_API_KEY')) if OpenAI else None

    def generate_response(self, user_message, memories, workflow_context):
        if not self.client:
            return MockAI().generate_response(user_message, memories, workflow_context)

        mem_str = '. '.join([f"{k}: {v}" for k, v in memories.items()]) or 'None yet.'
        system = (
            "You are The Arboreal Sage – a calm, wise, nature-inspired guide. "
            "Use metaphors about trees, seasons, and growth. Respond concisely, with empathy. "
            "Avoid jargon. Keep responses to 2-3 short paragraphs. "
            f"Known memories about the user: {mem_str}"
        )
        user_prompt = user_message
        if workflow_context:
            system += f" The user is currently in a \"{workflow_context.get('type')}\" workflow. Guide them to answer the current question without skipping ahead."
            user_prompt = f"[Workflow: {workflow_context.get('type')}] User says: {user_message}"

        try:
            completion = self.client.chat.completions.create(
                model='gpt-4o-mini',
                messages=[
                    {'role': 'system', 'content': system},
                    {'role': 'user', 'content': user_prompt}
                ],
                temperature=0.7,
                max_tokens=300
            )
            return completion.choices[0].message.content
        except Exception as e:
            print("OpenAI error:", e)
            return MockAI().generate_response(user_message, memories, workflow_context)

