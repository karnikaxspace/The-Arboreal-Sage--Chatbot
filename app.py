# 2. Workflow definitions & service
# ============================================================
WORKFLOWS = {
    'DAILY_DIG': {
        'name': 'Daily Dig',
        'steps': [
            {'id': 'objective', 'prompt': 'What is your main objective for today?'},
            {'id': 'obstacle', 'prompt': 'What obstacle stands in your way?'},
            {'id': 'seed', 'prompt': None}  # generated by AI
        ]
    },
    'REFLECTION': {
        'name': 'Reflection',
        'steps': [
            {'id': 'challenge', 'prompt': 'Tell me about a recent challenge you faced.'},
            {'id': 'sunlight', 'prompt': 'What sunlight (positive aspect) came from that challenge?'},
            {'id': 'growth', 'prompt': None}
        ]
    }
}

def start_workflow(sid, wf_type):
    if wf_type not in WORKFLOWS:
        return None
    state = {
        'type': wf_type,
        'step_index': 0,
        'answers': {},
        'is_active': True
    }
    set_workflow(sid, state)
    return WORKFLOWS[wf_type]['steps'][0]['prompt']

def advance_workflow(sid, user_message):
    state = get_workflow(sid)
    if not state or not state['is_active']:
        return None

    wf = WORKFLOWS[state['type']]
    step_idx = state['step_index']
    step = wf['steps'][step_idx]

    # Save answer
    state['answers'][step['id']] = user_message
    state['step_index'] += 1

    # Check if workflow is complete (we have all except the final AI‑generated step)
    if state['step_index'] >= len(wf['steps']) - 1:
        state['is_active'] = False
        set_workflow(sid, state)
        # store answers
        save_memory(sid, f"workflow_{state['type'].lower()}", state['answers'])
        for k, v in state['answers'].items():
            save_memory(sid, k, v)
        return {'done': True, 'answers': state['answers'], 'type': state['type']}
    else:
        next_step = wf['steps'][state['step_index']]
        set_workflow(sid, state)
        return {'done': False, 'prompt': next_step['prompt'], 'step': next_step['id']}

def is_workflow_active(sid):
    state = get_workflow(sid)
    return state is not None and state.get('is_active', False)

def get_current_prompt(sid):
    state = get_workflow(sid)
    if not state or not state['is_active']:
        return None
    wf = WORKFLOWS[state['type']]
    step = wf['steps'][state['step_index']]
    return step['prompt']

def generate_workflow_summary(wf_type, answers):
    if wf_type == 'DAILY_DIG':
        return (f"🌱 Today's Dig complete! Objective: \"{answers.get('objective', '')}\". "
                f"Obstacle: \"{answers.get('obstacle', '')}\". Now planting your seed...")
    elif wf_type == 'REFLECTION':
        return (f"🌿 Reflection complete. Challenge: \"{answers.get('challenge', '')}\". "
                f"Sunlight: \"{answers.get('sunlight', '')}\". Growing your insight...")
    return "Workflow complete."

