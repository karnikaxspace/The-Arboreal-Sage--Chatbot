# 1. In‑memory store (session‑scoped)
# ============================================================
store = {}  # key: session_id, value: {'memories': {}, 'workflow': None}

def get_store(sid):
    if sid not in store:
        store[sid] = {'memories': {}, 'workflow': None}
    return store[sid]

def save_memory(sid, key, value):
    data = get_store(sid)
    data['memories'][key] = value

def get_memory(sid, key):
    data = get_store(sid)
    return data['memories'].get(key)

def get_all_memories(sid):
    data = get_store(sid)
    return data['memories'].copy()

def set_workflow(sid, workflow_state):
    data = get_store(sid)
    data['workflow'] = workflow_state

def get_workflow(sid):
    data = get_store(sid)
    return data['workflow']

def clear_workflow(sid):
    data = get_store(sid)
    data['workflow'] = None

