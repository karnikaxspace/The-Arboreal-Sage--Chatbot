# 4. Intent detection helpers
# ============================================================
def detect_workflow_intent(text):
    t = text.lower()
    if re.search(r'(daily dig|plan my day|set.*objective|today\'?s? (goal|plan))', t):
        return 'DAILY_DIG'
    if re.search(r'(reflect|journal|reflection|process.*challenge)', t):
        return 'REFLECTION'
    return None

def is_memory_check(text):
    t = text.lower()
    return re.search(r'(how did .* go|check.*progress|my (root|objective)|last time|previous (goal|session)|root take root|check on my root)', t) is not None

def is_name_intro(text):
    match = re.search(r'my name is\s+([\w\s]+)', text, re.I)
    if match:
        return match.group(1).strip()
    return None

